package com.excilys.spring.mom.parser;

public class MOMResponseParsingException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6230843450999328205L;

	public MOMResponseParsingException() {
		super();
	}

	public MOMResponseParsingException(String message, Throwable cause) {
		super(message, cause);
	}

	public MOMResponseParsingException(String message) {
		super(message);
	}

	public MOMResponseParsingException(Throwable cause) {
		super(cause);
	}

}
